/**
 * @brief Reads a u16 field from a buffer.
 *
 * @param src The destination to read static data from.
 */
template <>
inline uint16_t read_field<>(const uint8_t* src, uint16_t idx) {
    src += idx * 2;
    uint16_t val = 0;
#if IS_BIG_ENDIAN == 0
    memcpy(&val, src, sizeof(val));
#elif IS_BIG_ENDIAN == 1
    memcpy(&val, src, sizeof(val));
    val = byteswap16(val);
#else
    val |= src[0] << 0;
    val |= src[1] << 8;
#endif
    return val;
}

